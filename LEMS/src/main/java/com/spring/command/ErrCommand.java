package com.spring.command;

public class ErrCommand {
	private String preUse;
	private String realUse;
	private String hwCode;
	public String getPreUse() {
		return preUse;
	}
	public void setPreUse(String preUse) {
		this.preUse = preUse;
	}
	public String getRealUse() {
		return realUse;
	}
	public void setRealUse(String realUse) {
		this.realUse = realUse;
	}
	public String getHwCode() {
		return hwCode;
	}
	public void setHwCode(String hwCode) {
		this.hwCode = hwCode;
	}
	
}
